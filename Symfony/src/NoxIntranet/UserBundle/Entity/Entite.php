<?php

namespace NoxIntranet\UserBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Entite
 *
 * @ORM\Table()
 * @ORM\Entity(repositoryClass="NoxIntranet\UserBundle\Entity\EntiteRepository")
 */
class Entite {

    /**
     * @var integer
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="societe", type="string", length=255)
     */
    private $societe;

    /**
     * @var string
     *
     * @ORM\Column(name="etablissement", type="string", length=255)
     */
    private $etablissement;

    /**
     * @var string
     *
     * @ORM\Column(name="aa", type="string", length=255)
     */
    private $aa;

    /**
     * @var string
     *
     * @ORM\Column(name="da", type="string", length=255)
     */
    private $da;
    
    /**
     * @var string
     *
     * @ORM\Column(name="arh", type="string", length=255)
     */
    private $arh;
    
    /**
     * @var string
     *
     * @ORM\Column(name="n2", type="string", length=255)
     */
    private $n2;
    
    
    /**
     * Get id
     *
     * @return integer
     */
    public function getId() {
        return $this->id;
    }

    /**
     * Set societe
     *
     * @param string $societe
     *
     * @return Entite
     */
    public function setSociete($societe) {
        $this->societe = $societe;

        return $this;
    }

    /**
     * Get societe
     *
     * @return string
     */
    public function getSociete() {
        return $this->societe;
    }

    /**
     * Set etablissement
     *
     * @param string $etablissement
     *
     * @return Entite
     */
    public function setEtablissement($etablissement) {
        $this->etablissement = $etablissement;

        return $this;
    }

    /**
     * Get etablissement
     *
     * @return string
     */
    public function getEtablissement() {
        return $this->etablissement;
    }

    /**
     * Set aa
     *
     * @param string $aa
     *
     * @return Entite
     */
    public function setAa($aa) {
        $this->aa = $aa;

        return $this;
    }

    /**
     * Get aa
     *
     * @return string
     */
    public function getAa() {
        return $this->aa;
    }
    
    /**
     * Set da
     *
     * @param string $da
     *
     * @return Entite
     */
    public function setDa($da) {
        $this->da = $da;

        return $this;
    }

    /**
     * Get da
     *
     * @return string
     */
    public function getDa() {
        return $this->da;
    }
    
    /**
     * Set arh
     *
     * @param string $arh
     *
     * @return Entite
     */
    public function setArh($arh) {
        $this->arh = $arh;

        return $this;
    }

    /**
     * Get arh
     *
     * @return string
     */
    public function getArh() {
        return $this->arh;
    }
    
    /**
     * Set n2
     *
     * @param string $n2
     *
     * @return Entite
     */
    public function setN2($n2) {
        $this->n2 = $n2;

        return $this;
    }

    /**
     * Get n2
     *
     * @return string
     */
    public function getN2() {
        return $this->n2;
    }

}
